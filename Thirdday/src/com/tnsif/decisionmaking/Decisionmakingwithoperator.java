package com.tnsif.decisionmaking;

public class Decisionmakingwithoperator {

	public static void main(String[] args) {
		 
		int a = 10;
        int b = 5;

    
        if ((a + b) > 12) {
            System.out.println("Sum is greater than 12.");
        } else {
            System.out.println("Sum is not greater than 12.");
        }

    
        if (a > 0 && b > 0) {
            System.out.println("Both a and b are positive.");
        } else {
            System.out.println("One or both numbers are not positive.");
        }

       
        if (a == b) {
            System.out.println("a and b are equal.");
        } else {
            System.out.println("a and b are not equal.");
        }

    }
}






